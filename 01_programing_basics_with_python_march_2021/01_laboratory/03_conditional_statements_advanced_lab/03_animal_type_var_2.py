#input

animal = input()

#variable

animal_type = ""

#variable statements

if animal == "dog":
    animal_type = "mammal"

elif animal == "crocodile":
    animal_type = "reptile"

elif animal == "tortoise":
    animal_type = "reptile"

elif animal == "snake":
    animal_type = "reptile"

else:
    animal_type = "unknown"

# output

print(animal_type)
